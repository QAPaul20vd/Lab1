package com.company.lab5.AnimalRescue;

public class Cat extends Animal {

    public void purr(){
        System.out.println("This cat purrs.");
    }

    public void scratch(){
        System.out.println("This cat has big claws and likes to scratch everything.");
    }

    public void speak(){
        System.out.println("The cat says: miau, miau!");
    }

    public void drink(){
        System.out.println("The cat drinks more milk than water.");
    }
}
